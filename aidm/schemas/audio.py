@dataclass
class AudioAttribution:
    """Attribution record for curated audio asset.

    Tracks CC BY and royalty-free assets requiring attribution.
    """

    asset_id: str
    """Unique asset identifier (matches AudioTrack.source_file)."""

    asset_type: Literal["music", "sfx", "ambient"]
    """Asset type."""

    title: Optional[str] = None
    """Track title (for music)."""

    artist: Optional[str] = None
    """Artist/composer name."""

    source: str = ""
    """Source identifier (e.g., 'Kevin MacLeod', 'Sonniss GDC 2024', 'Freesound')."""

    license: str = ""
    """License identifier (e.g., 'CC BY 3.0', 'CC0', 'Royalty-free')."""

    url: Optional[str] = None
    """Source URL (for CC BY attribution requirements)."""

    requires_attribution: bool = True
    """True if license requires attribution (CC BY, royalty-free with attribution clause)."""

    def to_dict(self) -> Dict[str, Any]:
        """Convert to dictionary for JSON serialization."""
        return {
            "asset_id": self.asset_id,
            "asset_type": self.asset_type,
            "title": self.title,
            "artist": self.artist,
            "source": self.source,
            "license": self.license,
            "url": self.url,
            "requires_attribution": self.requires_attribution,
        }

    @classmethod
    def from_dict(cls, data: Dict[str, Any]) -> "AudioAttribution":
        """Create from dictionary."""
        return cls(
            asset_id=data.get("asset_id", ""),
            asset_type=data.get("asset_type", "music"),  # type: ignore
            title=data.get("title"),
            artist=data.get("artist"),
            source=data.get("source", ""),
            license=data.get("license", ""),
            url=data.get("url"),
            requires_attribution=data.get("requires_attribution", True),
        )

    def format_attribution(self) -> str:
        """Format attribution string for credits/ATTRIBUTION.txt.

        Returns:
            Human-readable attribution string
        """
        if not self.requires_attribution:
            return ""

        parts = []

        if self.title:
            parts.append(f'"{self.title}"')

        if self.artist:
            parts.append(f"by {self.artist}")

        if self.source:
            parts.append(f"({self.source})")

        if self.license:
            parts.append(f"— {self.license}")

        if self.url:
            parts.append(f"<{self.url}>")

        return " ".join(parts)


@dataclass
class AudioAttributionLedger:
    """Ledger of all audio attributions for a campaign.

    Tracks all CC BY and royalty-free assets requiring attribution.
    Generates ATTRIBUTION.txt for bundled assets.
    """

    attributions: List[AudioAttribution] = field(default_factory=list)
    """List of attribution records."""

    def add_attribution(self, attribution: AudioAttribution) -> None:
        """Add attribution record to ledger."""
        self.attributions.append(attribution)

    def get_attributions_by_type(
        self, asset_type: Literal["music", "sfx", "ambient"]
    ) -> List[AudioAttribution]:
        """Get all attributions of a specific type.

        Args:
            asset_type: Asset type to filter by

        Returns:
            List of matching attributions
        """
        return [a for a in self.attributions if a.asset_type == asset_type]

    def generate_attribution_text(self) -> str:
        """Generate ATTRIBUTION.txt content.

        Returns:
            Formatted attribution text for bundled assets
        """
        lines = ["=" * 80, "AUDIO ASSET ATTRIBUTIONS", "=" * 80, ""]

        # Music attributions
        music_attrs = self.get_attributions_by_type("music")
        if music_attrs:
            lines.append("MUSIC:")
            lines.append("")
            for attr in music_attrs:
                if attr.requires_attribution:
                    lines.append(f"  {attr.format_attribution()}")
            lines.append("")

        # SFX attributions
        sfx_attrs = self.get_attributions_by_type("sfx")
        if sfx_attrs:
            lines.append("SOUND EFFECTS:")
            lines.append("")
            for attr in sfx_attrs:
                if attr.requires_attribution:
                    lines.append(f"  {attr.format_attribution()}")
            lines.append("")

        # Ambient attributions
        ambient_attrs = self.get_attributions_by_type("ambient")
        if ambient_attrs:
            lines.append("AMBIENT AUDIO:")
            lines.append("")
            for attr in ambient_attrs:
                if attr.requires_attribution:
                    lines.append(f"  {attr.format_attribution()}")
            lines.append("")

        lines.append("=" * 80)
        return "\n".join(lines)

    def to_dict(self) -> Dict[str, Any]:
        """Convert to dictionary for JSON serialization."""
        return {
            "attributions": [a.to_dict() for a in self.attributions],
        }

    @classmethod
    def from_dict(cls, data: Dict[str, Any]) -> "AudioAttributionLedger":
        """Create from dictionary."""
        attributions = [
            AudioAttribution.from_dict(a) for a in data.get("attributions", [])
        ]
        return cls(attributions=attributions)
